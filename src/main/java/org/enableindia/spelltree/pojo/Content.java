package org.enableindia.spelltree.pojo;

public class Content {
	
	private int Content_Id;
	private String Content_String;
	private String Content_Meaning;
	private String Content_Example;
	private String Content_Link_Id;
	
	public int getContent_Id() {
		return Content_Id;
	}
	
	public void setContent_Id(int content_Id) {
		Content_Id = content_Id;
	}
	
	public String getContent_String() {
		return Content_String;
	}
	
	public void setContent_String(String content_String) {
		Content_String = content_String;
	}
	
	public String getContent_Meaning() {
		return Content_Meaning;
	}
	
	public void setContent_Meaning(String content_Meaning) {
		Content_Meaning = content_Meaning;
	}
	
	public String getContent_Example() {
		return Content_Example;
	}
	
	public void setContent_Example(String content_Example) {
		Content_Example = content_Example;
	}
	
	public String getContent_Link_Id() {
		return Content_Link_Id;
	}
	
	public void setContent_Link_Id(String content_Link_Id) {
		Content_Link_Id = content_Link_Id;
	}
	
}
